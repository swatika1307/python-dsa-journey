# Class creation
class Employee:
    # name = "Swatika"
    language = "Python"
    salary = 1200000

# Object
swatika = Employee()
swatika.name = "Swatika"
print(swatika.name, swatika.language, swatika.salary)

yash = Employee()
yash.name = "Yash"
print(yash.name, yash.salary, yash.language)

# Here, name is object attribute; language and salary are class attributes